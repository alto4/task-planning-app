const asyncHandler = require('express-async-handler');
const { findById, findByIdAndUpdate } = require('../models/taskModel');

const Task = require('../models/taskModel');
const User = require('../models/userModel');

// GET - retrieve all tasks
// /api/tasks
const getTasks = asyncHandler(async (req, res) => {
  const tasks = await Task.find({ user: req.user.id });
  console.log('all tasks => ', tasks);

  res.status(200).json({
    tasks,
  });
});

// POST - create a new task
// /api/tasks
const createTask = asyncHandler(async (req, res) => {
  if (!req.body.title) {
    res.status(400);
    throw new Error('Please add a task title.');
  }

  const task = await Task.create({
    user: req.user.id,
    title: req.body.title,
    description: req.body.description,
    category: req.body.category,
    estimatedPomodoros: req.body.estimatedPomodoros,
    completedPomodoros: req.body.completedPomodoros,
  });

  res.status(200).json(task);
});

// PUT - update an existing tasks
// /api/tasks/:id
const updateTask = asyncHandler(async (req, res) => {
  try {
    const task = await Task.findById(req.params.id);
    const allTasks = await Task.find({ user: req.user.id });

    console.log('req,body for update, mark complete => ', req.body);
    if (!task) {
      res.status(400);
      throw new Error('Task not found.');
    }

    // Check for user
    const user = await User.findById(req.user.id);

    if (!user) {
      res.status(401);
      throw new Error('User not found.');
    }

    // Make sure authenticated user matches goal user
    if (task.user.toString() !== user.id) {
      res.status(401);
      throw new Error('User not authorized.');
    }

    const updatedTask = await Task.findByIdAndUpdate(req.params.id, req.body, { new: true });

    console.log('updatedTask => ', updateTask);
    console.log('allTasks => ', allTasks);
    const updatedTasks = allTasks.map((task) =>
      task._id.toString() === updatedTask?._id.toString() ? updatedTask : task
    );
    console.log('updatedTask => ', updatedTask);
    res.status(200).json(updatedTasks);
  } catch (error) {
    console.error('Error updating task => ', error);
  }
});

// DELETE - delete an existing task
// /api/tasks/:id
const deleteTask = asyncHandler(async (req, res) => {
  const task = await Task.findById(req.params.id);

  if (!task) {
    res.status(400);
    throw new Error('Task not found.');
  }

  // Check for user
  const user = await User.findById(req.user.id);

  if (!user) {
    res.status(401);
    throw new Error('User not found.');
  }

  // Make sure authenticated user matches goal user
  if (task.user.toString() !== user.id) {
    res.status(401);
    throw new Error('User not authorized.');
  }

  await Task.findByIdAndRemove(req.params.id);

  res.status(200).json({ id: req.params.id });
});

module.exports = {
  getTasks,
  createTask,
  updateTask,
  deleteTask,
};
